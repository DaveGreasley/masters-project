   end subroutine benchgeo_setup_channel


  !=======================================================================
  !
  !     define a sphere packing in a tube
  !
  !=======================================================================

  subroutine benchgeo_setup_packing(gx,gy,gz,cidx)

    implicit none

    integer, intent(in)  :: gx,gy,gz
    integer, intent(out) :: cidx(gx,gy,gz)

    integer n,i,j,k,inlet,outlet
    real    res,y_center,z_center,ra,x,y,z

    ! all cells are fluid by default
    cidx(:,:,:) = 1

    res = ( min(gy,gz)-2 ) / 5.0
    y_center = (gy+1) / 2.d0
    z_center = (gz+1) / 2.d0
    ra = ( min(gy,gz)-2 )*( min(gy,gz)-2 ) / 4.0

    ! generate cylinder
    do j = 1, gy
       do k = 1, gz
          if ( (j-y_center)**2+(k-z_center)**2 .ge. ra) then
             do i = 1, gx
                cidx(i,j,k) = 0
             end do
          end if
       end do
    end do

    inlet  = min(gy,gz) / 2
    outlet = min(gy,gz) / 2

    ! check all spheres
    do n = 1, n_spheres

       x = sphere_coords(1,n)
       y = sphere_coords(2,n)-2.5
       z = sphere_coords(3,n)-2.5

       if ( x .lt. 2 .or. x .gt. 22 ) cycle
       x = x - 2

       do i = int((x-0.5)*res)-1, int((x+0.5)*res)+1
          if ( i .lt. (gx-outlet-inlet) ) then
             do j = int(y_center+(y-0.5)*res)-1, int(y_center+(y+0.5)*res)+1
                do k = int(z_center+(z-0.5)*res)-1, int(z_center+(z+0.5)*res)+1
                   if ( ( (i-x*res)**2 + (j-y_center-y*res)**2 +               &
                          (k-z_center-z*res)**2 ) .le. 0.5*0.5*res*res ) then
                      cidx(i+inlet,j,k) = 0
                   end if
                end do   ! k
             end do   ! j
          end if
       end do   ! i
    end do   ! all spheres

  end subroutine benchgeo_setup_packing


  !=======================================================================
  !
  !     define a simple synthetic porous medium ("Siegrid-3D")
  !
  !=======================================================================

  subroutine benchgeo_setup_synpor(gx,gy,gz,cidx,minx,maxx,div,rem)

    implicit none

    integer, intent(in)  :: gx,gy,gz,minx,maxx,div,rem
    integer, intent(out) :: cidx(gx,gy,gz)

    integer i,j,k

    ! all cells are fluid by default
    cidx(:,:,:) = 1

    do k = 1, gz
       do j = 1, gy
          do i = minx, maxx

             if ( mod(i,div).ge.0 .and. mod(i,div).le.rem .and.                &
                  mod(j,div).ge.0 .and. mod(j,div).le.rem .and.                &
                  mod(k,div).ge.0 .and. mod(k,div).le.rem ) then

                cidx(i,j,k) = 0

             end if

          end do
       end do
    end do

  end subroutine benchgeo_setup_synpor

